    private void hapusData() {
    int row = TBLbiodata.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(null, "Pilih baris yang akan dihapus!");
        return;
    }

    // Ambil nilai nim dari data yang dipilih
    String nim = TBLbiodata.getValueAt(row, 2).toString(); // Ambil nim dari kolom kedua (index 2)

    // Buat query SQL untuk menghapus data dari tbl_prodi
    String sqlProdi = "DELETE FROM tbl_prodi WHERE nim=?";

    try {
        // Siapkan prepared statement untuk tbl_prodi
        PreparedStatement psProdi = conn.prepareStatement(sqlProdi);
        psProdi.setString(1, nim);

        // Eksekusi statement untuk tbl_prodi
        int hasilProdi = psProdi.executeUpdate();

        // Buat query SQL untuk menghapus data dari tbl_biodata
        String sqlBiodata = "DELETE FROM tbl_biodata WHERE nim=?";
        PreparedStatement psBiodata = conn.prepareStatement(sqlBiodata);
        psBiodata.setString(1, nim);

        // Eksekusi statement untuk tbl_biodata
        int hasilBiodata = psBiodata.executeUpdate();

        // Tampilkan pesan sukses
        if (hasilBiodata > 0 && hasilProdi > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            tampilData();
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menghapus data. \n" + e.getMessage());
    }
}

    private void tampilData() {
    DefaultTableModel kolomtabel = new DefaultTableModel();
    kolomtabel.addColumn("No.");
    kolomtabel.addColumn("Nama");
    kolomtabel.addColumn("NIM");
    kolomtabel.addColumn("TTL");
    kolomtabel.addColumn("Jenis Kelamin");
    kolomtabel.addColumn("Prodi");
    kolomtabel.addColumn("Nomor Telepon");
    kolomtabel.addColumn("Alamat");

    try {
        int nomor = 1;
        // buat objek statement untuk mengeksekusi query mysql.
        st = conn.createStatement();
        // buat query ke database, gabungkan data dari kedua tabel
        String sql = "SELECT b.nama, b.nim, b.ttl, b.jekel, l.prodi, l.telepon, l.alamat "
                   + "FROM tbl_biodata b "
                   + "JOIN tbl_prodi l ON b.nim = l.nim"; // Sesuaikan dengan hubungan antara kedua tabel

        // eksekusi query dan simpan hasilnya di objek ResultSet
        rs = st.executeQuery(sql);

        // tampilkan hasil query menggunakan perulangan while
        while (rs.next()) {
            kolomtabel.addRow(new Object[]{
                ("" + nomor++),
                rs.getString("nama"),
                rs.getString("nim"),
                rs.getString("ttl"),
                rs.getString("jekel"),
                rs.getString("prodi"),
                rs.getString("telepon"),
                rs.getString("alamat"),
            });
        }

        TBLbiodata.setModel(kolomtabel);
        TBLbiodata.enable(true);
        tfNama.requestFocus();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menampilkan data. \n" + e.getMessage());
    }
}


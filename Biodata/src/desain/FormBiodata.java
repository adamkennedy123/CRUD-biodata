    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        if (tfNama.getText().equals("")
            || tfNim.getText().equals("")
            || tfTtl.getText().equals("")
            || tfNomortelp.getText().equals("")
            || txAlamat.getText().equals("")
            || buttonGroup1.getSelection() == null
            || cbProdi.getSelectedItem().equals("-- Program Studi --")) {
            JOptionPane.showMessageDialog(this, "Field harap di isi !",
                "Validasi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            String cekDB = "SELECT * FROM tbl_biodata WHERE nim = '"
            + tfNim.getText() + "' ";
            rs = st.executeQuery(cekDB);
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "NIM sudah tersedia !");
            } else {
                st = conn.createStatement();
                String jekel;
                if (rbLaki.isSelected()) {
                    jekel = "Laki - Laki";
                } else {
                    jekel = "Perempuan";
                }
                String sqlBiodata = "INSERT INTO tbl_biodata (nama, nim, ttl, jekel) VALUES ('"
                + tfNama.getText()
                + "', '" + tfNim.getText()
                + "', '" + tfTtl.getText()
                + "', '" + jekel
                + "') ";
                st.executeUpdate(sqlBiodata);

                String sqlLainnya = "INSERT INTO tbl_prodi (nim, prodi, telepon, alamat) VALUES ('"
                + tfNim.getText()
                + "', '" + cbProdi.getSelectedItem()
                + "', '" + tfNomortelp.getText()
                + "', '" + txAlamat.getText()
                + "') ";
                st.executeUpdate(sqlLainnya);
                JOptionPane.showMessageDialog(null, "Data berhasil di simpan");
                tampilData();
                clearForm();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSubmitActionPerformed


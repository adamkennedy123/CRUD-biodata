    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (nimToUpdate == null || nimToUpdate.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diubah!");
            return;
        }

        try {
            String jekel;
            if (rbLaki.isSelected()) {
                jekel = "Laki - Laki";
            } else {
                jekel = "Perempuan";
            }

            // Create query to update data
            String sqlBiodata = "UPDATE tbl_biodata SET nama=?, ttl=?, jekel=? WHERE nim=?";
            PreparedStatement psBiodata = conn.prepareStatement(sqlBiodata);
            psBiodata.setString(1, tfNama.getText());
            psBiodata.setString(2, tfTtl.getText());
            psBiodata.setString(3, jekel);
            psBiodata.setString(4, nimToUpdate);

            // Execute update query
            int hasilBiodata = psBiodata.executeUpdate();

            String sqlLainnya = "UPDATE tbl_prodi SET prodi=?, telepon=?, alamat=? WHERE nim=?";
            PreparedStatement psLainnya = conn.prepareStatement(sqlLainnya);
            psLainnya.setString(1, cbProdi.getSelectedItem().toString());
            psLainnya.setString(2, tfNomortelp.getText());
            psLainnya.setString(3, txAlamat.getText());
            psLainnya.setString(4, nimToUpdate);

            // Execute update query
            int hasilLainnya = psLainnya.executeUpdate();

            // Show success message
            if (hasilBiodata > 0 && hasilLainnya > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                tampilData();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

